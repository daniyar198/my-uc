<p style="text-align: center;">
 <b>ПРАВИЛА </b><br>
 <b>
	внутреннего распорядка </b><b>обучающихся </b>
</p>
<p>
</p>
<p>
</p>
<p style="text-align: justify;">
	 1. Общие положения&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 1.1. Настоящие Правила внутреннего распорядка имеют целью регулирования отношений внутри ГОУ ДПО Учебный центр «Симай» (далее – Учебный центр), создание эффективной организации учебного процесса, рационального использования учебного времени, обеспечение высокого качества оказываемых услуг.&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 1.2. Правила внутреннего распорядка обязательны для всех обучающихся в Учебном центре.&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 1.3. Правила внутреннего распорядка соответствуют действующему законодательству Российской Федерации и разработаны на основе Устава, типовых Правил и нормативных актов.&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 1.4. Общее руководство Учебного центра осуществляет директор. Директор несет полную ответственность за выполнение учебных планов и программ, организацию учебного процесса, финансово-хозяйственную деятельность и охрану труда.&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2. Права и обязанности обучающихся&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.1. Учащиеся имеют право:&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.1.1. на получение образования в соответствии с образовательными программами;&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.1.2. на выбор форм получения образования (индивидуальная или групповая, дневная или вечерняя);&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.1.3. на ознакомление с настоящими Правилами и другими локальными актами, регламентирующими деятельность Учебного центра;&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.1.4. на обучение по индивидуальным учебным планам (если предусмотрено с образовательными программами);&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.1.5. на бесплатное пользование библиотечным фондом, иной материально-технической базой во время образовательного процесса;&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.1.6. на уважение человеческого достоинства, свободу совести и информации, свободное выражение своих взглядов и убеждений;&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.1.7. на защиту от применения методов физического и психического насилия;&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.1.8. на условия обучения, гарантирующие охрану и укрепление здоровья;&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.1.9. на внесение предложений по организации деятельности Учебного центра, улучшения санитарно-гигиенического обслуживания и обеспечения режима.&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.2. Учащиеся обязаны:&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.2.1. соблюдать Правила внутреннего распорядка для обучающихся и иные локальные акты для учащихся, исполнять решения и приказы директора Учебного центра;&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.2.2. уважать права, честь и достоинство других учащихся, работников Учебного центра, не допускать ущемление их интересов;&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.2.3. быть дисциплинированными, соблюдать общественный порядок в здании и на территории Учебного центра;&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.2.4. сознательно относиться к учебе, своевременно являться на занятия, соблюдатьпорядок на своем рабочем месте;&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.2.5. при неявке учащегося на занятия по болезни или другим уважительным причинам, учащийся обязан в течение первого дня болезни поставить об этом в известность работников Учебного центра или преподавателя;&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.2.6. беречь имущество Учебного центра, бережно относиться к результатам труда других людей;&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.2.7. экономно расходовать электроэнергию, воду, сырье и другие материалы.&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.3. Учащимся УСЦ запрещается:&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.3.1. приносить, передавать или использовать оружие, спиртные напитки, табачные изделия, токсические и наркотические вещества;&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.3.2. использовать любые средства и вещества, которые могут привести к взрывам и пожарам;&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.3.3. производить любые иные действия, влекущие за собой опасные последствия для окружающих и самого обучающегося;&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.3.4. выносить без разрешения администрации учебного центра инвентарь, оборудование из кабинетов и других помещений;&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.3.5. курить в помещениях Учебного центра и на территории здания.&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 3. Правила посещения&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 3.1. Приходить в Учебный центр следует за 5 - 10 минут до начала занятий в чистой одежде, иметь опрятный вид.&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 3.2. Необходимо иметь с собой и все необходимые для занятий принадлежности.&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 3.3. Перед началом занятий учащиеся должны свериться с расписанием и прибыть вкабинет до преподавателя.&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 3.4. После окончания занятий нужно одеться и покинуть учебный центр, соблюдая правила вежливости.&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 4. Поведение на занятиях&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 4.1. Каждый преподаватель определяет специфические правила при проведении занятий по своему предмету, которые не должны противоречить нормативным документам. Эти правила обязательны для исполнения всеми обучающимися.&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 4.2. Перед началом занятия учащиеся должны подготовить свое рабочее место и все необходимое для работы на занятии.&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 4.3. Время занятий должно использоваться только для учебных целей. Во время занятия нельзя отвлекаться самому и отвлекать других посторонними разговорами и другими, не относящимися к занятиям, делами.&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 4.4. Если учащемуся необходимо выйти из класса, он должен попросить разрешения преподавателя.&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 4.5. Запрещается во время занятий пользоваться мобильными телефонами и другими устройствами, не относящимися к учебному процессу. Следует отключить и убрать все технические устройства (плееры, наушники, игровые приставки и пр.), перевести мобильный телефон в тихий режим и убрать его со стола.&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 4.6. В случае опоздания на занятие постучаться в дверь кабинета, зайти, поздороваться с преподавателем, извиниться за опоздание и попросить разрешения сесть на место.&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 5. Заключительные положения&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 5.1. Настоящие Правила могут быть изменены и дополнены в соответствии с нормативными актами уполномоченных федеральных органов исполнительной власти в области образования.&nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 5.2. Процедура внесения изменений и дополнений в настоящие правила аналогична процедуре их принятия.
